async def generate_seo_package(user_input: dict) -> str:
    prompt = _build_prompt(user_input)
    logger.info(f"Генерируем SEO для: {user_input.get('name')} [{user_input.get('marketplace')}]")

    message = client.messages.create(
        model="claude-haiku-4-5-20251001",
        max_tokens=2000,
        messages=[{"role": "user", "content": prompt}]
    )

    result = message.content[0].text
    header = (
        f"📦 *{user_input.get('name', 'Товар')}*\n"
        f"🏪 {user_input.get('marketplace', 'Маркетплейс')} "
        f"{'· 💰 ' + str(user_input.get('price', '')) + '₽' if user_input.get('price') else ''}\n"
        f"{'─' * 30}\n\n"
    )
    return header + result


